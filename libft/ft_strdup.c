#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*new;

	i = 0;
	if (!s)
		return (NULL);
	while (s[i])
		i++;
	if (!(new = malloc(sizeof(char) * i + 1)))
		return (0);
	i = 0;
	while (s[i])
	{
		new[i] = s[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
